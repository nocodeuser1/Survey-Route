const CODE_TO_NAME = new Map(US_STATES.map(s => [s.code, s.name]));

export function getUSStateName(code: string | null | undefined): string | null {
  if (!code) return null;
  return CODE_TO_NAME.get(code.toUpperCase()) ?? null;
}

/** True when `code` is a recognised US state/territory code. Case-insensitive. */
export function isValidUSStateCode(code: string | null | undefined): boolean {
  if (!code) return false;
  return CODE_TO_NAME.has(code.toUpperCase());
}
